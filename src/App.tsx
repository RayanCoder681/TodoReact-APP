import { useState } from "react";
import { useEffect } from "react";
import TodoItem  from "./TodoItem";
import { Construction } from "lucide-react";

type Priority="Urgente" | "Moyenne" | "Basse"

type Todo = {
  id:  number;
  text: string;
  priority: Priority;
}

function App() {
  const [input ,setInput] = useState<string>("")
  const [priority ,setPriority] = useState<Priority>("Moyenne")

  const savedTodos = localStorage.getItem("todos")
  const initialTodos = savedTodos ? JSON.parse(savedTodos) : []
  const [todos ,setTodos] = useState<Todo[]>(initialTodos)
  const [filter ,setFilter]=useState<Priority | "Tous">("Tous")

  useEffect( () => {
    localStorage.setItem("todos", JSON.stringify(todos))
  }, [todos])


  function addTodo()  {

    if (input.trim() == "") {
      return
    }

      const newTodo: Todo = {
        id: Date.now(),
        text: input.trim(),
        priority: priority
      }

      const newTodos = [newTodo, ...todos]
      setTodos(newTodos)
      setInput("")
      setPriority("Moyenne")
      console.log(todos)
    }
     
    let filteredTodos : Todo[] = []
    if(filter === "Tous"){
      filteredTodos = todos
    }else{
      filteredTodos = todos.filter((todo) => todo.priority === filter)
    }

    const urgentCount = todos.filter((t) => t.priority === "Urgente").length
    const mediumCount = todos.filter((t) => t.priority === "Moyenne").length
    const lowCount = todos.filter((t) => t.priority === "Basse").length
    const totalCount=todos.length

    function deleteTodo(id:number) {
      const newTodo = todos.filter((todo) => todo.id !== id)
      setTodos(newTodo)
    }

    const [selectedTodos , setSelectedTodos] = useState<Set<number>>(new Set())
    
    function toggleSelectTodo(id :number){
      const newSelected = new Set(selectedTodos)
      if(newSelected.has(id)){
        newSelected.delete(id)
      }else{
        newSelected.add(id)
      }
      setSelectedTodos(newSelected)
    }

    function finishedSelected(){
      const newTodos = todos.filter((todo) => {
        if (selectedTodos.has(todo.id)){
          return false
        }else{
          return true
        }
      })
      setTodos(newTodos)
      setSelectedTodos(new Set())
    }
    
    return (

      <div className=" flex justify-center ">
          <div className="w-2/3 flex flex-col gap-4 my-20 bg-base-300 p-4 rounded-2x1 " >
              <div className="flex flex-row gap-3">
                  <label htmlFor="priority-select" className="sr-only">
                     Priorit√©
                  </label>
                  <input
                      type="text"
                      className="input w-full"
                      placeholder="Ajouter une tache..."
                      value ={input}
                      onChange ={(e) => setInput(e.target.value)}
                    />
                    <select 
                      id="priority-select"
                      className="select w-full "
                      value={priority}
                      onChange={(e) => setPriority(e.target.value as Priority)}
                    >
                      <option value="Urgente">Urgente</option>
                      <option value="Moyenne">Moyenne</option>
                      <option value="Basse">Basse</option>

                    </select>
                    <button
                    onClick={addTodo} 
                    className="btn btn-primary">
                        Ajouter
                    </button>
              </div>
              <div className="flex flex-col h-fit">
                <div className="flex justify-between gap-3">
                  <div className="flex gap-7">
                        <button
                          className={'btn ntn-soft ${filter === "Tous" ? "btn-primary" : ""}'}
                          onClick ={() => setFilter("Tous")}
                        >
                          Tous ({totalCount})
                          </button>
                          <button
                          className={'btn ntn-soft ${filter === "Urgente" ? "btn-primary" : ""}'}
                          onClick ={() => setFilter("Urgente")}
                        >
                          Urgente ({urgentCount})
                          </button>
                          <button
                          className={'btn ntn-soft ${filter === "Moyenne" ? "btn-primary" : ""}'}
                          onClick ={() => setFilter("Moyenne")}
                        >
                          Moyenne ({mediumCount})
                          </button>
                          <button
                          className={'btn ntn-soft ${filter === "Basse" ? "btn-primary" : ""}'}
                          onClick ={() => setFilter("Basse")}
                        >
                          Basse ({lowCount})
                          </button>
                       </div>
                      <div>
                        <button
                        onClick={finishedSelected}
                        className="btn btn-primary"
                        disabled={selectedTodos.size == 0}
                        >
                          Finir la Selection ({selectedTodos.size})
                        </button>
                  </div>
                </div>
                
                  { filteredTodos.length > 0 ? (
                    <ul className="divide-y divide-primary/20">
                        {filteredTodos.map((todo) => (
                          <li key={todo.id}>
                          <TodoItem 
                          onToggleSelect={toggleSelectTodo}
                          isSelected={selectedTodos.has(todo.id)}
                          todo={todo}
                          onDelete={() => deleteTodo(todo.id)}
                          />
                          </li>
                        ))}
                      </ul>
                      
                    ) : (
                      <div className="flex justify-center items-center flex-col p-5">
                        <div>
                          <Construction strokeWidth={1} className="w-40 h-40 text-primary"/>
                        </div>
                        <p className="text-sm">
                          Aucune tache pour ce fiiltre
                        </p>
                      </div>
                
                    )
                  }
              </div>
          </div>
      </div>
  )
}

export default App
